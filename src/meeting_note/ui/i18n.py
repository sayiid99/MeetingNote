def normalize_language(language: str | None) -> str:
    if not language:
        return DEFAULT_UI_LANGUAGE
    normalized = language.strip().lower()
    if normalized.startswith(UI_LANGUAGE_CHINESE):
        return UI_LANGUAGE_CHINESE
    return UI_LANGUAGE_ENGLISH


def translate(language: str | None, key: str, **kwargs: object) -> str:
    normalized = normalize_language(language)
    template = TRANSLATIONS.get(normalized, {}).get(key) or TRANSLATIONS[DEFAULT_UI_LANGUAGE].get(key) or key
    if kwargs:
        try:
            return template.format(**kwargs)
        except (KeyError, ValueError):
            return template
    return template


def language_display_options(language: str | None) -> Mapping[str, str]:
    normalized = normalize_language(language)
    return {
        UI_LANGUAGE_ENGLISH: translate(normalized, "settings.language_english"),
        UI_LANGUAGE_CHINESE: translate(normalized, "settings.language_chinese"),
    }
